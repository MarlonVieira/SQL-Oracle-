SELECT C.EMPRESA,
       C.CLIENTE,
       C.NOME,
       'GAB - ' || C.NOME || ' - RIEL' AS CONCATENACAO,
       UPPER(C.NOME) AS MAISCULAS,
       LOWER(C.NOME) AS MINUSCULAS,
       COALESCE(C.APELIDO, 'SEM VALOR') AS TRATAVLRNULOS,
       REPLACE(C.NOME, 'ELOI', 'JAO') AS SUBSTITUICAOCHAR,
       SUBSTR(C.NOME, 1, 4) AS COPIACHAR,
       SUBSTRING(C.NOME FROM 5) AS COPIACHAR2,
       CHAR_LENGTH(C.NOME) AS QTDCHAR,
       LPAD(C.NOME, 60, '!') AS ADDCHARESQUERDA,
       RPAD(C.NOME, 60, '!') AS ADDCHARDIREITA,
       POSITION(';' IN (C.NOME || ';')) AS POSICAODOTEXTOPROCURADO ,
       POS(';', (C.NOME || ';')) AS POSVIASOFT,
       TRIM('   ' || C.NOME || '   ') AS REMOVEESPACOBRANCO,
       '   ' || C.NOME || '   ' AS SEMREMOVERESPBRANCO,
       IIF(CHAR_LENGTH(C.NOME) < 25, 'MENOR Q 25', 'SOU MAIOR') AS CONDICIONAL_SE
  FROM PPESCLI C